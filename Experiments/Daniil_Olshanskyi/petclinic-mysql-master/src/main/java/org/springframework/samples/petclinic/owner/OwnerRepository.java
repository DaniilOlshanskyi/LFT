package org.springframework.samples.petclinic.owner;

import java.util.List;

import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.CrudRepository;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;

/**
 * Repository class for <code>Owners</code> domain objects All method names are compliant with Spring Data naming
 * conventions so this interface can easily be extended for Spring Data See here: http://static.springsource.org/spring-data/jpa/docs/current/reference/html/jpa.repositories.html#jpa.query-methods.query-creation
 *
 * @author Ken Krebs
 * @author Juergen Hoeller
 * @author Sam Brannen
 * @author Michael Isvy
 * @ModifiedBy Tanmay Ghosh
 */
@Repository
public interface OwnerRepository extends JpaRepository<Owners, Integer> {

    @Query("SELECT o FROM Owners o LEFT OUTER JOIN City c on (o.city=c.city)")
    public List<Owners> leftOuterJoin();

    @Query("SELECT o FROM Owners o RIGHT OUTER JOIN City c on (o.city=c.city)")
    public List<Owners> rightOuterJoin();
}
